package it.esercizi;

public class Taniche {
	private int litri;
	private int[] contenuto;
			
	public Taniche(int litri, int contenuto) {
		this.litri = litri;
		this.contenuto = this.contenuto;
	}
	
	
	public int riempi(int[] contenuto, int i, int litri) {
		while(i <= 2) {
			i++;
			i = contenuto[i];
		}
		return litri;
	}
	
	public int versa() {
		return litri;
	}
	
	public int svuota() {
		 while (litri >= 0) {
			 litri--;
			 litri = 0;
		 }
		 return litri;
	}
}
	
